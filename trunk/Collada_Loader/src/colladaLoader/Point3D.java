//-------------------------------------------------------------------------
//---------------------------------------------------------- Vertex Storage
//-------------------------------------------------------------------------

public class Point3D{

  float x, y, z;
  
  Point3D(){
	  
  }

  Point3D(float _x, float _y, float _z){

	  this.x = _x;
	  this.y = _y;
	  this.z = _z;

  }
}